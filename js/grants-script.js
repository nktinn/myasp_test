cb1.onclick = ()=>{
    let check = cb1.querySelector("input");
    let img = cb1.querySelector(".checkbox_img");
    check.checked = !check.checked;
    if(check.checked == true){
        img.style.visibility = "visible";
    }
    else{
        img.style.visibility = "hidden";
    }

}

